 /**
         * The WordGames class provides methods for word manipulation.
         *
         * @author giovanni
         */
        public class WordGames {

            /**
             * Adds "Hello " before the provided word.
             *
             * @param word The word to be prefixed with "Hello ".
             * @return The resulting string with "Hello ".
             */
            public String addHelloWord(String word) {
                return "Hello " + word;
            }

            /**
             * Concatenates the provided name and surname.
             *
             * @param name    The first name.
             * @param surname The last name.
             * @return The full name as a concatenated string.
             */
            public String getFullName(String name, String surname) {
                return name + " " + surname;
            }
        }



